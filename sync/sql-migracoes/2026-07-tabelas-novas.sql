-- Cria SOMENTE as tabelas e os indices novos: nenhuma tabela existente e tocada, e o
-- dashboard continua servindo o espelho atual enquanto isto roda.
--
-- Depois de aplicar, carregue as novas (na maquina que alcanca o Oracle):
--   python sync/agente.py PCATIVI PCMARCA PCREGIAO PCSUPERV PCGERENTE PCEMPR --                         PCCIDADE PCPLPAG PCCONTA PCDIASUTEIS PCITEM PCLANC
